# range is a datatype as well as it is a function
# it represnts sequence of value

# range one argument
x=range(10)
print(x)
for i in x: print(i)
print("************************")
# range two arguments
x=range(10,20)
print(x)
for i in x: print(i)
print("*************************")
# range three arguments (n1,n2,step)
for i in range(0,20,2): print(i)
